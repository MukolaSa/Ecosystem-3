const Heading = () => {
  const [selectedCategory, setSelectedCategory] = useState("Всі");
  const [searchText, setSearchText] = useState("");

  // Фільтрація шаблонів на основі категорії та пошуку
  const filteredTemplates = templates.filter((template) => {
    const matchesCategory =
      selectedCategory === "Всі" || template.category === selectedCategory;
    const matchesSearch = template.text.toLowerCase().includes(searchText.toLowerCase());
    return matchesCategory && matchesSearch;
  });

  // Функція копіювання тексту в буфер обміну
  const handleCopy = (text) => {
    navigator.clipboard.writeText(text);
    alert(`Скопійовано: "${text}"`);
  };

  return (
    <div style={{ padding: "20px" }}>
      <h1>Бібліотека CTA та Заголовків</h1>

      {/* Вибір категорії */}
      <Select onChange={(e) => setSelectedCategory(e.target.value)} value={selectedCategory}>
        <option value="Всі">Всі категорії</option>
        <option value="Продажі">Продажі</option>
        <option value="Реєстрація">Реєстрація</option>
        <option value="Залучення">Залучення</option>
        <option value="Новини">Новини</option>
        <option value="Емоції">Емоції</option>
      </Select>

      {/* Пошук шаблонів */}
      <input
        type="text"
        placeholder="Пошук..."
        value={searchText}
        onChange={(e) => setSearchText(e.target.value)}
        style={{ marginLeft: "10px", padding: "5px" }}
      />

      {/* Список шаблонів */}
      <div style={{ marginTop: "20px" }}>
        {filteredTemplates.map((template) => (
          <div
            key={template.id}
            style={{
              display: "flex",
              justifyContent: "space-between",
              padding: "10px",
              borderBottom: "1px solid #ccc"
            }}
          >
            <span>{template.text}</span>
            <button onClick={() => handleCopy(template.text)}>Копіювати</button>
          </div>
        ))}
      </div>
    </div>
  );
};

export default Heading;